package gian.Model;

public class Result {
    public int id;
    public String title;
    public int readyInMinutes;
    public int servings;
    public String sourceUrl;
    public int openLicense;
    public String image;

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public int getReadyInMinutes() {
        return readyInMinutes;
    }

    public int getServings() {
        return servings;
    }

    public String getSourceUrl() {
        return sourceUrl;
    }

    public int getOpenLicense() {
        return openLicense;
    }

    public String getImage() {
        return image;
    }
}
